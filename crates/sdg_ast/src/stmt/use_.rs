use crate::ast::NameOrigin;
use crate::expr::{Expr, ExprKind, InfixOp};
use crate::path::Path;
use crate::session::{InternedString, LocalParseSession};
use crate::span::Span;
use crate::token::{Token, TokenKind};
use crate::value::ValueKind;
use std::slice::Iter;

#[derive(Clone)]
pub struct Use {
    pub path: Path,
    pub alias: InternedString,

    pub span: Span,  // keyword `use`
}

impl Use {
    pub fn new(path: Vec<(InternedString, Span)>, alias: InternedString, span: Span) -> Self {
        assert!(!path.is_empty(), "Internal Compiler Error C992CA92C26");

        Use {
            path: Path::from_names(path),
            alias, span
        }
    }

    pub fn get_first_name(&self) -> InternedString {
        self.path.get_name_by_index(0)
    }

    pub fn append_front(mut self, path: &Vec<(InternedString, Span)>) -> Self {
        self.path.append_front(&path);

        self
    }

    // used for resolving names.
    // Exprs generated by this function do not have spans: be careful
    // not to generate errors from Exprs from this
    pub fn to_path(&self, origin: NameOrigin) -> ExprKind {
        to_path_impl(self.path.as_ref(), origin)
    }

    pub fn iter_path(&self) -> Iter<(InternedString, Span)> {
        self.path.as_ref().iter()
    }

    pub fn dump(&self, session: &LocalParseSession) -> String {
        #[cfg(test)]
        if self.span.dump(session) != "use" {
            panic!("{}", self.span.render_err(session));
        }
        // assert_eq!(self.span.dump(session), "use");

        format!("use {} as {};", self.path.dump(session), self.alias.to_string(session))
    }
}

pub fn use_case_to_tokens(Use { path, alias, span }: Use) -> Vec<Token> {
    // `use`, PATH, `as`, ALIAS, `;`
    let mut tokens = Vec::with_capacity(path.len() * 2 + 3);

    tokens.push(Token {
        span,
        kind: TokenKind::keyword_use(),
    });

    for token in path.tokens() {
        tokens.push(token);
    }

    tokens.push(Token {
        span,
        kind: TokenKind::keyword_as(),
    });

    tokens.push(Token {
        span,
        kind: TokenKind::Identifier(alias),
    });

    tokens.push(Token {
        span,
        kind: TokenKind::semi_colon(),
    });

    tokens
}

macro_rules! new_path {
    (leaf, $p0: expr, $origin: expr) => {
        ExprKind::Value(ValueKind::Identifier($p0, $origin))
    };
    (recurs, $ph: expr, $pt: expr) => {
        ExprKind::Infix(
            InfixOp::Path,
            Box::new(Expr {
                kind: $ph,
                span: Span::dummy(),
            }),
            Box::new(Expr {
                kind: new_path!(leaf, $pt.0, NameOrigin::SubPath),
                span: $pt.1,
            })
        )
    };
    ($p0: expr, $origin: expr) => {
        new_path!(leaf, $p0.0, $origin)
    };
    ($p0: expr, $p1: expr, $origin: expr) => {
        ExprKind::Infix(
            InfixOp::Path,
            Box::new(Expr {
                kind: new_path!(leaf, $p0.0, $origin),
                span: $p0.1,
            }),
            Box::new(Expr {
                kind: new_path!(leaf, $p1.0, NameOrigin::SubPath),
                span: $p1.1,
            }),
        )
    };
    ($p0: expr, $p1: expr, $p2: expr, $origin: expr) => {
        new_path!(recurs, new_path!($p0, $p1, $origin), $p2)
    };
    ($p0: expr, $p1: expr, $p2: expr, $p3: expr, $origin: expr) => {
        new_path!(recurs, new_path!($p0, $p1, $p2, $origin), $p3)
    };
    ($p0: expr, $p1: expr, $p2: expr, $p3: expr, $p4: expr, $origin: expr) => {
        new_path!(recurs, new_path!($p0, $p1, $p2, $p3, $origin), $p4)
    };
}

// the best optimization I can think of
fn to_path_impl(path: &[(InternedString, Span)], origin: NameOrigin) -> ExprKind {
    assert!(!path.is_empty(), "Internal Compiler Error DD4626E9B3A");

    if path.len() < 4 {

        if path.len() == 1 {
            new_path!(path[0], origin)
        }

        else if path.len() == 2 {
            new_path!(path[0], path[1], origin)
        }

        else {
            new_path!(path[0], path[1], path[2], origin)
        }

    }

    else {

        if path.len() == 4 {
            new_path!(path[0], path[1], path[2], path[3], origin)
        }

        else if path.len() == 5 {
            new_path!(path[0], path[1], path[2], path[3], path[4], origin)
        }

        else {
            new_path!(recurs, to_path_impl(&path[0..(path.len() - 1)], origin), path[path.len() - 1])
        }

    }
}
